      SUBROUTINE SUB_TRIPGEN4
      IMPLICIT INTEGER(A-Z)
C***********************************************************************
C        1         2         3         4         5         6         7
C23456789012345678901234567890123456789012345678901234567890123456789012
C***********************************************************************
C
C     THIS SUBROUTINE NOW ONLY PRINTS THE HOUSEHOLD PRODUCTIONS AND 
C     ATTRACTIONS FROM THE ENUMERATED HOUSEHOLDS MATCHED WITH TRAVEL 
C     SURVEY HOOSEHOLDS
C
C     EASH, APRIL 2012
C
C***********************************************************************
C
C     THIS IS THE FOURTH PROGRAM IN THE REVISED HOUSEHOLD TRIP
C     GENERATION.  IT MULTIPLIES THE WORKER, NONWORKER AND 
C     CHILD TRIP GENERATION RATES TIMES THE HOUSEHOLDS (MORE CORRECTLY
C     THE PERSONS IN EACH HOUSEHOLD) TO OBTAIN HOUSEHOLD TRIP
C     PRODUCTIONS, WHICH NOW EQUAL ATTRACTIONS.  THE OUTPUT FILE
C     CONTAINS TRIP PRODUCTIONS FOR EACH TG SUBZONE.
C
C     THIS VERSION IS FOR SUN FORTRAN. 
C
C     EASH, AUGUST 1995
C
C***********************************************************************
C
C     REVISIONS FOR PRAIRIE PARKWAY TRIP GENERATION IN THIS VERSION.
C     1.  CHANGES IN INPUT-OUTPUT FILE NAMES AND FORMATS
C     2.  DESIGNED FOR SOCIOECONOMIC ZONES PREPARED BY SUHAIL ALCHALABI
C         FOR PRAIRIE PARKWAY (COMBINATION OF TG SUBZONES AND 
C         SECTIONS.
C     
C     EASH, JUNE 2003
C
C***********************************************************************
C
C     REVISED FOR I-290 HOV WORK IN NOVEMBER 2006.  CHANGED TO 
C     ACCOMODATE NEW TEMPORARY FILE FORMATS AND PUMS SUMMARIES.
C
C     EASH, NOVEMBER 2006
C
C***********************************************************************
C
C     REVISED FOR CMAP TRIP GENERATION UPDATE IN JANUARY 2009. 
C     SUBROUTINE REVISED TO ACCOMODATE NEW CMAP SURVEY TRIP RATES AND 
C     NEW SET OF TRIP TYPES.  OTHER CHANGES NOTED IN BODY OF SUBROUTINE,
C     ALSO SEE TEXT FILE TGRATES.FI
C
C     EASH,  JANUARY 2009
C
C***********************************************************************
      INCLUDE 'COMMON_PARAM.FI'
	INCLUDE 'COMMON_HHSUMS.FI'
	INCLUDE 'COMMON_GEOG.FI'
      INCLUDE 'COMMON_HIHHS.FI'
      INCLUDE 'COMMON_PUMSHHS.FI'
      INCLUDE 'COMMON_HHMISC0.FI'
C***********************************************************************
C
C     PRODUCTION ARRAY
C
C       PRODS(1-20,1) = WORKER HH TRIP PRODUCTIONS FOR NO VEHICLE, NO 
C                       CHILDREN HHS
C       PRODS(1-20,2) = WORKER HH TRIP PRODUCTIONS FOR NO VEHICLE, WITH 
C                       CHILDREN HHS
C       PRODS(1-20,3) = WORKER HH TRIP PRODUCTIONS FOR WITH VEHICLE, NO 
C                       CHILDREN HHS
C       PRODS(1-20,4) = WORKER HH TRIP PRODUCTIONS FOR WITH VEHICLE,  
C                       WITH CHILDREN HHS
C       PRODS(21-33,1) = NONWORKER HH TRIP PRODUCTIONS FOR NO VEHICLE,  
C                        NO CHILDREN HHS
C       PRODS(21-33,2) = NONWORKER HH TRIP PRODUCTIONS FOR NO VEHICLE,  
C                        WITH CHILDREN HHS
C       PRODS(21-33,3) = WORKER HH TRIP PRODUCTIONS FOR WITH VEHICLE,  
C                        NO CHILDREN HHS
C       PRODS(21-33,4) = WORKER HH TRIP PRODUCTIONS FOR WITH VEHICLE,  
C                        WITH CHILDREN HHS
C       PRODS(34-49,1) = CHILD 12-15 HH TRIP PRODUCTIONS FOR NO VEHICLE
C                        HHS
C       PRODS(34-49,2) = CHILD 12-15 HH TRIP PRODUCTIONS FOR WITH 
C                        VEHICLE HHS
C
C     NOTE THAT EXTRA PRODUCTIONS IN WORKER PRODS ARE FOR HIGH (ABOVE 
C     MEDIAN) AND LOW (BELOW MEDIAN) EARNINGS WORKERS PRODUCTIONS
C
C***********************************************************************
      REAL*4 PRODS(25000,49,4), SUM_HH(25000,4)
C
	INTEGER*4 TOTAL_HH_IN
      INTEGER*4 HISERNO
C -- Heither, 04-15-2018, no longer character      CHARACTER*8 HISERNO
      INTEGER*4  HITRIPS(49)
C
C     INPUT FILE FROM HOUSEHOLD ENUMERATION IS UNIT 42
C
	REWIND 42
C
C     OUTPUT FILE ON UNIT 34 OR 36 CONTAINS TRIP PRODUCTIONS
C
      OPEN (UNIT=44,FILE='PRODS_HH_OUT.TXT',STATUS='NEW',ERR=944)
C
C     START LOOPING, READ A TG SUBZONE RECORD FROM INPUT FILES
C
      WRITE (*,'(/A/)') ' START OF BIG TG SUBZONE LOOP'

      TOTAL_HH_IN = 0.0

      COUNT = 0
C
      DO SZ = 1,25000
        DO J=1,4 
	    SUM_HH(SZ,J) = 0.0 
          DO I=1,49
            PRODS(SZ,I,J) = 0.0
          ENDDO
        ENDDO
      ENDDO        
C
   11 CONTINUE

      COUNT = COUNT + 1
C
C     READ TG SUBZONE HOUSEHOLD FILE
C       
      READ (42,'(I5,I7,2I4,2I2,I8,55I3)',END=19) PUMS_SZ(COUNT),
     A  PUMS_PUMA5(COUNT), PUMS_HHTYPE(COUNT), PUMS_HHVTYPE(COUNT),
     B  PUMS_VEH(COUNT), PUMS_ROWCOL(COUNT), HISERNO, HIADULT, HIWORKER,
     C  HINONWORKER, HICHILD, HICHILD1215, HIVEH, (HITRIPS(J),J=1,49)
      
C        WRITE (16,'(I5,I7,2I4,2I2,A,49I3)') PUMS_SZ(COUNT),
C     A    PUMS_PUMA5(COUNT), PUMS_HHTYPE(COUNT), PUMS_HHVTYPE(COUNT),
C     B    PUMS_VEH(COUNT), PUMS_ROWCOL(COUNT), HISERNO,HIADULT,HIWORKER,
C     C    HINONWORKER, HICHILD, HICHILD1215, HIVEH, (HITRIPS(J),J=1,49)  
      
        IF (MOD(COUNT,25000) .EQ. 0) 
     A    WRITE(*,'(A,I8)')' TG SUBZONE HOUSEHOLD RECORDS READ= ',COUNT
      
      TOTAL_HH_IN = TOTAL_HH_IN + 1
C
C     IN THE FOLLOWING SUMMARIES PRODS1 ARE THE ORIGINAL 11 TRIP 
C     TYPES (J=1 IS WITHOUT VEHICLES AND J=2 IS WITH VEHICLES) AND 
C     PRODS2 ARE THE EXPANDED 49 TRIP TYPE BY FOUR CATEGORIES
C
C     1 = NO CHILDREN, NO VEHICLES
C     2 = CHILDREN, NO VEHICLES
C     3 = NO CHILDREN, VEHICLES
C     4 = CHILDREN, VEHICLES
C
      SZ = PUMS_SZ(COUNT)
          
	CAT = 4
      IF ((HICHILD .EQ. 0) .AND. (HIVEH .EQ. 0)) CAT = 1
	IF ((HICHILD .GT. 0) .AND. (HIVEH .EQ. 0)) CAT = 2
	IF ((HICHILD .EQ. 0) .AND. (HIVEH .GT. 0)) CAT = 3

	SUM_HH(SZ,CAT) = SUM_HH(SZ,CAT) + 1
C  
      DO PD = 1,49
        PRODS(SZ,PD,CAT) = PRODS(SZ,PD,CAT) + HITRIPS(PD)   
      ENDDO      
C
C     GEOGRAPHY
C
      ICO = 0
	IICO = SZ_CO(SZ)
	DO J=1,COUNTIES
        IF (IICO .EQ. CO_NUM(J)) ICO = J
      ENDDO

	IP1 = 0
	IIP1 = SZ_PUMA1(SZ)
	DO J=1,PUMA1
	  IF (IIP1 .EQ. P1_NUM(J)) IP1 = J
	ENDDO
	 
	IP5 = 0
	IIP5 = SZ_PUMA5(SZ)
	DO J=1,PUMA5
	  IF (IIP5 .EQ. P5_NUM(J)) IP5 = J
      ENDDO
C
C     ACCUMULATE PERSONS
C
C     IN THE FOLLOWING SUMMARIES WORKER1, ETC ARE  ORIGINAL 11 TRIP 
C     TYPES (J=1 IS WITHOUT VEHICLES AND J=2 IS WITH VEHICLES) AND 
C     WORKER2, ETC ARE EXPANDED 49 TRIP TYPE BY ABOVE FOUR CATEGORIES
C
C     1 = NO CHILDREN, NO VEHICLES
C     2 = CHILDREN, NO VEHICLES
C     3 = NO CHILDREN, VEHICLES
C     4 = CHILDREN, VEHICLES
C
      IF (HIVEH .EQ. 0) THEN
          
	  CO_HH_WORKER1(ICO,1) = CO_HH_WORKER1(ICO,1) + HIWORKER
	  CO_HH_NONW1(ICO,1) = CO_HH_NONW1(ICO,1) + HINONWORKER
        CO_HH_CHILD1(ICO,1) = CO_HH_CHILD1(ICO,1) + HICHILD1215 
		
	  PUMA1_HH_WORKER1(IP1,1) = PUMA1_HH_WORKER1(IP1,1) + HIWORKER
	  PUMA1_HH_NONW1(IP1,1) = PUMA1_HH_NONW1(IP1,1) + HINONWORKER
        PUMA1_HH_CHILD1(IP1,1) = PUMA1_HH_CHILD1(IP1,1) + HICHILD1215
		
        PUMA5_HH_WORKER1(IP5,1) = PUMA5_HH_WORKER1(IP5,1) + HIWORKER
 	  PUMA5_HH_NONW1(IP5,1) = PUMA5_HH_NONW1(IP5,1) + HINONWORKER
        PUMA5_HH_CHILD1(IP5,1) = PUMA5_HH_CHILD1(IP5,1) + HICHILD1215
		
        TOT_HH_WORKER1(1) = TOT_HH_WORKER1(1) + HIWORKER
        TOT_HH_NONW1(1) = TOT_HH_NONW1(1) + HINONWORKER	
        TOT_HH_CHILD1(1) = TOT_HH_CHILD1(1) + HICHILD1215

        IF(SZ_CHI(SZ) .EQ. 1) THEN
          CHI_HH_WORKER1(1) = CHI_HH_WORKER1(1) + HIWORKER
		CHI_HH_NONW1(1) = CHI_HH_NONW1(1) + HINONWORKER
		CHI_HH_CHILD1(1) = CHI_HH_CHILD1(1) + HICHILD1215
	  ENDIF	

        IF(SZ_CBD(SZ) .EQ. 1) THEN
          CBD_HH_WORKER1(1) = CBD_HH_WORKER1(1) + HIWORKER
		CBD_HH_NONW1(1) = CBD_HH_NONW1(1) + HINONWORKER
		CBD_HH_CHILD1(1) = CBD_HH_CHILD1(1) + HICHILD1215
	  ENDIF	
      ELSE
          
	  CO_HH_WORKER1(ICO,2) = CO_HH_WORKER1(ICO,2) + HIWORKER
	  CO_HH_NONW1(ICO,2) = CO_HH_NONW1(ICO,2) + HINONWORKER
        CO_HH_CHILD1(ICO,2) = CO_HH_CHILD1(ICO,2) + HICHILD1215
		
	  PUMA1_HH_WORKER1(IP1,2) = PUMA1_HH_WORKER1(IP1,2) + HIWORKER
	  PUMA1_HH_NONW1(IP1,2) = PUMA1_HH_NONW1(IP1,2) + HINONWORKER
        PUMA1_HH_CHILD1(IP1,2) = PUMA1_HH_CHILD1(IP1,2) + HICHILD1215
		
	  PUMA5_HH_WORKER1(IP5,2) = PUMA5_HH_WORKER1(IP5,2) + HIWORKER
	  PUMA5_HH_NONW1(IP5,2) = PUMA5_HH_NONW1(IP5,2) + HINONWORKER
        PUMA5_HH_CHILD1(IP5,2) = PUMA5_HH_CHILD1(IP5,2) + HICHILD1215
		
        TOT_HH_WORKER1(2) = TOT_HH_WORKER1(2) + HIWORKER
	  TOT_HH_NONW1(2) = TOT_HH_NONW1(2) + HINONWORKER
	  TOT_HH_CHILD1(2) = TOT_HH_CHILD1(2) + HICHILD1215

        IF(SZ_CHI(SZ) .EQ. 1) THEN
          CHI_HH_WORKER1(2) = CHI_HH_WORKER1(2) + HIWORKER
		CHI_HH_NONW1(2) = CHI_HH_NONW1(2) + HINONWORKER
		CHI_HH_CHILD1(2) = CHI_HH_CHILD1(2) + HICHILD1215
	  ENDIF	

        IF(SZ_CBD(SZ) .EQ. 1) THEN
          CBD_HH_WORKER1(2) = CBD_HH_WORKER1(2) + HIWORKER
		CBD_HH_NONW1(2) = CBD_HH_NONW1(2) + HINONWORKER
		CBD_HH_CHILD1(2) = CBD_HH_CHILD1(2) + HICHILD1215
	  ENDIF	
      ENDIF	  
C
	CO_HH_WORKER2(ICO,CAT) = CO_HH_WORKER2(ICO,CAT) + HIWORKER
	CO_HH_NONW2(ICO,CAT) = CO_HH_NONW2(ICO,CAT) + HINONWORKER 
      CO_HH_CHILD2(ICO,CAT) = CO_HH_CHILD2(ICO,CAT) + HICHILD1215
		
	PUMA1_HH_WORKER2(IP1,CAT) = PUMA1_HH_WORKER2(IP1,CAT) + 
     A  HIWORKER
	PUMA1_HH_NONW2(IP1,CAT) = PUMA1_HH_NONW2(IP1,CAT) + 
     A  HINONWORKER
      PUMA1_HH_CHILD2(IP1,CAT) = PUMA1_HH_CHILD2(IP1,CAT) + 
     A  HICHILD1215
		
	PUMA5_HH_WORKER2(IP5,CAT) = PUMA5_HH_WORKER2(IP5,CAT) + 
     A  HIWORKER
 	PUMA5_HH_NONW2(IP5,CAT) = PUMA5_HH_NONW2(IP5,CAT) + 
     A  HINONWORKER
      PUMA5_HH_CHILD2(IP5,CAT) = PUMA5_HH_CHILD2(IP5,CAT) + 
     A  HICHILD1215
		
      TOT_HH_WORKER2(CAT) = TOT_HH_WORKER2(CAT) + HIWORKER
	TOT_HH_NONW2(CAT) = TOT_HH_NONW2(CAT) + HINONWORKER
	TOT_HH_CHILD2(CAT) = TOT_HH_CHILD2(CAT) + HICHILD1215

      IF(SZ_CHI(SZ) .EQ. 1) THEN
        CHI_HH_WORKER2(CAT) = CHI_HH_WORKER2(CAT) + HIWORKER
        CHI_HH_NONW2(CAT) = CHI_HH_NONW2(CAT) + HINONWORKER
	  CHI_HH_CHILD2(CAT) = CHI_HH_CHILD2(CAT) + HICHILD1215
	ENDIF	

      IF(SZ_CBD(SZ) .EQ. 1) THEN
        CBD_HH_WORKER2(CAT) = CBD_HH_WORKER2(CAT) + HIWORKER
	  CBD_HH_NONW2(CAT) = CBD_HH_NONW2(CAT) + HINONWORKER
	  CBD_HH_CHILD2(CAT) = CBD_HH_CHILD2(CAT) + HICHILD1215
      ENDIF
C
C     RETURN FOR ANOTHER TG SUBZONE
C
      GO TO 11
   19 CONTINUE      
C
C     ACCUMULATE SUMMARIES
C
C     IN THE FOLLOWING SUMMARIES PRODS1 ARE THE ORIGINAL 11 TRIP 
C     TYPES (J=1 IS WITHOUT VEHICLES AND J=2 IS WITH VEHICLES) AND 
C     PRODS2 ARE THE EXPANDED 49 TRIP TYPE BY ABOVE FOUR CATEGORIES
C
C     1 = NO CHILDREN, NO VEHICLES
C     2 = CHILDREN, NO VEHICLES
C     3 = NO CHILDREN, VEHICLES
C     4 = CHILDREN, VEHICLES
C
      DO SZ = 1,SUBZONES
C
C     GEOGRAPHY
C
        ICO = 0
	  IICO = SZ_CO(SZ)
	  DO J=1,COUNTIES
          IF (IICO .EQ. CO_NUM(J)) ICO = J
        ENDDO

	  IP1 = 0
	  IIP1 = SZ_PUMA1(SZ)
	  DO J=1,PUMA1
	    IF (IIP1 .EQ. P1_NUM(J)) IP1 = J
        ENDDO
	 
        IP5 = 0
	  IIP5 = SZ_PUMA5(SZ)
	  DO J=1,PUMA5
	    IF (IIP5 .EQ. P5_NUM(J)) IP5 = J
        ENDDO
          
        DO J=1,2   
C
C     COUNTY TOTALS ORIGINAL TRIP TYPES
C
	    CO_HH_PRODS1(ICO,1,1) = CO_HH_PRODS1(ICO,1,1)+PRODS(SZ,1,J)+ 
     A      PRODS(SZ,2,J)+PRODS(SZ,3,J)+PRODS(SZ,4,J) 
	    CO_HH_PRODS1(ICO,1,2) = CO_HH_PRODS1(ICO,1,2)+PRODS(SZ,1,J+2)+ 
     A      PRODS(SZ,2,J+2)+PRODS(SZ,3,J+2)+PRODS(SZ,4,J+2)

	    CO_HH_PRODS1(ICO,2,1) = CO_HH_PRODS1(ICO,2,1)+PRODS(SZ,7,J)
	    CO_HH_PRODS1(ICO,2,2) = CO_HH_PRODS1(ICO,2,2)+PRODS(SZ,7,J+2)

	    CO_HH_PRODS1(ICO,3,1) = CO_HH_PRODS1(ICO,3,1)+PRODS(SZ,5,J)+
     A      PRODS(SZ,6,J)
	    CO_HH_PRODS1(ICO,3,2) = CO_HH_PRODS1(ICO,3,2)+PRODS(SZ,5,J+2)+ 
     A      PRODS(SZ,6,J+2)

	    CO_HH_PRODS1(ICO,4,1) = CO_HH_PRODS1(ICO,4,1)+PRODS(SZ,10,J)
	    CO_HH_PRODS1(ICO,4,2) = CO_HH_PRODS1(ICO,4,2)+PRODS(SZ,10,J+2)

	    CO_HH_PRODS1(ICO,5,1) = CO_HH_PRODS1(ICO,5,1)+PRODS(SZ,8,J)+
     A      PRODS(SZ,9,J)
	    CO_HH_PRODS1(ICO,5,2) = CO_HH_PRODS1(ICO,5,2)+PRODS(SZ,8,J+2)+ 
     A      PRODS(SZ,9,J+2)

	    CO_HH_PRODS1(ICO,6,1) = CO_HH_PRODS1(ICO,6,1)+PRODS(SZ,11,J)
	    CO_HH_PRODS1(ICO,6,2) = CO_HH_PRODS1(ICO,6,2)+PRODS(SZ,11,J+2)

	    CO_HH_PRODS1(ICO,7,1) = CO_HH_PRODS1(ICO,7,1)+PRODS(SZ,12,J)+
     A      PRODS(SZ,13,J)+PRODS(SZ,14,J)+PRODS(SZ,15,J)+
     B      PRODS(SZ,16,J)+PRODS(SZ,17,J)+PRODS(SZ,18,J)+
     C      PRODS(SZ,19,J)+PRODS(SZ,20,J)
	    CO_HH_PRODS1(ICO,7,2) = CO_HH_PRODS1(ICO,7,2)+
     A      PRODS(SZ,12,J+2)+PRODS(SZ,13,J+2)+PRODS(SZ,14,J+2)+
     B      PRODS(SZ,15,J+2)+PRODS(SZ,16,J+2)+PRODS(SZ,17,J+2)+
     C      PRODS(SZ,18,J+2)+PRODS(SZ,19,J+2)+PRODS(SZ,20,J+2)

	    CO_HH_PRODS1(ICO,8,1) = CO_HH_PRODS1(ICO,8,1)+PRODS(SZ,24,J)
	    CO_HH_PRODS1(ICO,8,2) = CO_HH_PRODS1(ICO,8,2)+PRODS(SZ,24,J+2)

	    CO_HH_PRODS1(ICO,9,1) = CO_HH_PRODS1(ICO,9,1)+PRODS(SZ,21,J)+
     A      PRODS(SZ,22,J)+PRODS(SZ,23,J)
	    CO_HH_PRODS1(ICO,9,2) = CO_HH_PRODS1(ICO,9,2)+
     A      PRODS(SZ,21,J+2)+PRODS(SZ,22,J+2)+PRODS(SZ,23,J+2)

	    CO_HH_PRODS1(ICO,10,1) = CO_HH_PRODS1(ICO,10,1)+
     A      PRODS(SZ,25,J)+PRODS(SZ,26,J)+PRODS(SZ,27,J)+
     B      PRODS(SZ,28,J)+PRODS(SZ,29,J)+PRODS(SZ,30,J)+
     C      PRODS(SZ,31,J)+PRODS(SZ,32,J)+PRODS(SZ,33,J)
	    CO_HH_PRODS1(ICO,10,2) = CO_HH_PRODS1(ICO,10,2)+
     A      PRODS(SZ,25,J+2)+PRODS(SZ,26,J+2)+PRODS(SZ,27,J+2)+
     B      PRODS(SZ,28,J+2)+PRODS(SZ,29,J+2)+PRODS(SZ,30,J+2)+
     C      PRODS(SZ,31,J+2)+PRODS(SZ,32,J+2)+PRODS(SZ,33,J+2)

	    CO_HH_PRODS1(ICO,11,1) = CO_HH_PRODS1(ICO,11,1)+
     A      PRODS(SZ,34,J)+PRODS(SZ,35,J)+PRODS(SZ,36,J)+
     B      PRODS(SZ,37,J)
     	    CO_HH_PRODS1(ICO,11,2) = CO_HH_PRODS1(ICO,11,2)+
     A      PRODS(SZ,34,J+2)+PRODS(SZ,35,J+2)+PRODS(SZ,36,J+2)+
     B      PRODS(SZ,37,J+2)
C
C     CHICAGO AND CBD SUMMARIES
C
          IF (SZ_CHI(SZ) .EQ. 1) THEN
	      CHI_HH_PRODS1(1,1) = CHI_HH_PRODS1(1,1)+PRODS(SZ,1,J)+
     A        PRODS(SZ,2,J)+PRODS(SZ,3,J)+PRODS(SZ,4,J)
	      CHI_HH_PRODS1(1,2) = CHI_HH_PRODS1(1,2)+PRODS(SZ,1,J+2)+
     A        PRODS(SZ,2,J+2)+PRODS(SZ,3,J+2)+PRODS(SZ,4,J+2)

	      CHI_HH_PRODS1(2,1) = CHI_HH_PRODS1(2,1)+PRODS(SZ,7,J)
	      CHI_HH_PRODS1(2,2) = CHI_HH_PRODS1(2,2)+PRODS(SZ,7,J+2)

	      CHI_HH_PRODS1(3,1) = CHI_HH_PRODS1(3,1)+PRODS(SZ,5,J)+
     A        PRODS(SZ,6,J)
	      CHI_HH_PRODS1(3,2) = CHI_HH_PRODS1(3,2)+PRODS(SZ,5,J+2)+
     A        PRODS(SZ,6,J+2)

	      CHI_HH_PRODS1(4,1) = CHI_HH_PRODS1(4,1)+PRODS(SZ,10,J)
	      CHI_HH_PRODS1(4,2) = CHI_HH_PRODS1(4,2)+PRODS(SZ,10,J+2)

	      CHI_HH_PRODS1(5,1) = CHI_HH_PRODS1(5,1)+PRODS(SZ,8,J)+
     A        PRODS(SZ,9,J)
	      CHI_HH_PRODS1(5,2) = CHI_HH_PRODS1(5,2)+PRODS(SZ,8,J+2)+
     A        PRODS(SZ,9,J+2)

	      CHI_HH_PRODS1(6,1) = CHI_HH_PRODS1(6,1)+PRODS(SZ,11,J)
	      CHI_HH_PRODS1(6,2) = CHI_HH_PRODS1(6,2)+PRODS(SZ,11,J+2)

	      CHI_HH_PRODS1(7,1) = CHI_HH_PRODS1(7,1)+PRODS(SZ,12,J)+
     A        PRODS(SZ,13,J)+PRODS(SZ,14,J)+PRODS(SZ,15,J)+
     B        PRODS(SZ,16,J)+PRODS(SZ,17,J)+PRODS(SZ,18,J)+
     C        PRODS(SZ,19,J)+PRODS(SZ,20,J)
	      CHI_HH_PRODS1(7,2) = CHI_HH_PRODS1(7,2)+
     A        PRODS(SZ,12,J+2)+PRODS(SZ,13,J+2)+PRODS(SZ,14,J+2)+
     B        PRODS(SZ,15,J+2)+PRODS(SZ,16,J+2)+PRODS(SZ,17,J+2)+
     C        PRODS(SZ,18,J+2)+PRODS(SZ,19,J+2)+PRODS(SZ,20,J+2)

	      CHI_HH_PRODS1(8,1) = CHI_HH_PRODS1(8,1)+PRODS(SZ,24,J)
	      CHI_HH_PRODS1(8,2) = CHI_HH_PRODS1(8,2)+PRODS(SZ,24,J+2)

	      CHI_HH_PRODS1(9,1) = CHI_HH_PRODS1(9,1)+PRODS(SZ,21,J)+
     A        PRODS(SZ,22,J)+PRODS(SZ,23,J)
	      CHI_HH_PRODS1(9,2) = CHI_HH_PRODS1(9,2)+
